// Continue Statement

class transfer2
{
	public static void main (String[] args)
	{
		for(int i=1; i<=5; i++)
		{
			if(i==3)
			{
				System.out.println("The value of i = "+i);
				continue;
			}
		}
	}
}
		
/*
	The continue statement is responsible to skip all the statement and move the control at the end of for loop.
*/