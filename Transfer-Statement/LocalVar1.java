// Example of Local variable

class LocalVar1
{
	public static void main (String[] args)
	{
		int a = 100;				// Local variable
		System.out.println("Start");
		System.out.println("Local variable = "+a);	// Direct access of Local variable
		System.out.println("Stop");
	}
}


/*
	-Variables declared within  a method or constructor or within a block (static/non-static) 
	 such variables are called as Local variables.
	-We can access local variables directly.
	-It is mandatory to initialize local variables before printing
*/