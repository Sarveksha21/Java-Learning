// Switch case if we don't use break statement.
/*
   If we do not use break statement then from which ever case it matches, from that statement onwards to till the switch all statement will get executed.
*/

class Switch8
{
	public static void main (String[] args)
	{
		int num = 1;
		switch(num)
		{
			case 1: System.out.println("One");
			case 2: System.out.println("Two");
			case 3: System.out.println("Three");
			default: System.out.println("Default Value");
		}
	}
}
	
			
						  