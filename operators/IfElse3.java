// Write a java program to check a person is eligible for marriage or not.

class IfElse3
{
	public static void main (String[] args)
	{
		int age = 20;
		char gender ='m';
		if(gender=='m' && age>=21)
		{
			System.out.println("Eligible for Marriage");
		}
		else
		{
			System.out.println("Not Eligible for Marriage");
		}
	}
}
