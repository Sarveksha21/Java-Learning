// Write a java program to stimulate traffic signals.

class IfElseIf2
{
	public static void main(String[] args)
	{
		char signal = 'r';
		if(signal=='r')
		{
			System.out.println("Stop");
		}
		else if(signal=='g')
		{
			System.out.println("Go");
		}
		else if(signal=='y')
		{
			System.out.println("Slow Down");
		}
		else
		{
			System.out.println("Invalid Signal");
		}
	}
}

			