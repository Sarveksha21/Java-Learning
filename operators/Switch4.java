// Switch statement without case statement or default statement
/*
	Default statement is not mandatory inside switch statement.
*/

class Switch4
{
	public static void main (String[] args)
	{
		int a=5;
		switch(a)
		{
			case 1: System.out.println("Inside Case One");
				break;
			case 2: System.out.println("Inside Case Two");
				break;
			case 3: System.out.println("Inside Case Three");
				break;
			
		}
	}
}