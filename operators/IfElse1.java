// Write a java program to check out whether a person's age is eligible for voting or not.

class IfElse1
{
	public static void main (String[] args)
	{
		int age=27;
		if(age >= 18)
		{
			System.out.println("Eligible for Voting");
		}
		else
		{
			System.out.println("Not Eligible for Voting");
		}
	}
}
